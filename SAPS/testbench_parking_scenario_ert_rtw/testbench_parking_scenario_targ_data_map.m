    ;% Auto data (jepusaxxp5a)
    ;%
      section.nData     = 2;
      section.data(2)  = dumData; %prealloc
      
	  ;% jepusaxxp5a.hqrbepkroo.TimePtr
	  section.data(1).logicalSrcIdx = 0;
	  section.data(1).dtTransOffset = 0;
	
	  ;% jepusaxxp5a.hu4yw4xjsy.TimePtr
